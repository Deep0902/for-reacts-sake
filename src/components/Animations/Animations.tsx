function Samurai() {
  const samuraiRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const samurai = samuraiRef.current;
    if (!samurai) return;

    let classes: string[] = [];
    let keys: { [key: string]: boolean } = {};

    const removeClasses = () => {
      classes.forEach((classe) => {
        if (classe !== "samurai") {
          samurai.classList.remove(classe);
        }
      });
    };

    const setIdle = () => {
      removeClasses();
      samurai.classList.add("idle");
    };

    const handleKeyDown = (ev: KeyboardEvent) => {
      classes = Array.from(samurai.classList);

      removeClasses();
      keys[ev.key] = true;

      switch (ev.key) {
        case "d":
          samurai.style.transform = "scaleX(4) scaleY(4)";
          samurai.classList.add("run");
          break;
        case "a":
          samurai.style.transform = "scaleX(-4) scaleY(4)";
          samurai.classList.add("run");
          break;
        case "k":
          samurai.classList.add("attack");
          break;
      }
    };

    const handleKeyUp = (ev: KeyboardEvent) => {
      keys[ev.key] = false;
      setIdle();
    };

    const interval = setInterval(() => {
      for (let key in keys) {
        if (!keys["a"] && !keys["d"] && !keys["k"]) {
          setIdle();
        }
      }
    }, 100);

    window.addEventListener("keydown", handleKeyDown);
    window.addEventListener("keyup", handleKeyUp);

    // Initialize with idle state
    setIdle();

    return () => {
      window.removeEventListener("keydown", handleKeyDown);
      window.removeEventListener("keyup", handleKeyUp);
      clearInterval(interval);
    };
  }, []);

  return (
    <main className="main">
      <header className="header">
        <h2 className="subheading">Press A or D to move, K to strike!</h2>
      </header>
      <div ref={samuraiRef} className="samurai idle"></div>
    </main>
  );
}
export { Samurai }; // Exporting Samurai component
